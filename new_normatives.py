def build_normative_lookup() -> dict:
    """
    Строит плоский словарь: subsidy_type -> normative (тенге за единицу)
    для быстрого маппинга по полю subsidy_type из датасета.
    """
    lookup = {}
    for direction, items in NORMATIVES.items():
        for item in items:
            lookup[item["subsidy_type"]] = item["normative"]
    return lookup

def build_deadline_lookup() -> dict:
    """
    Строит словарь: subsidy_type -> (month_start, day_start, month_end, day_end)
    для проверки соблюдения сроков подачи.
    """
    lookup = {}
    for direction, items in NORMATIVES.items():
        for item in items:
            start = item["deadline_start"]  
            end = item["deadline_end"]
            sm, sd = int(start.split("-")[0]), int(start.split("-")[1])
            em, ed = int(end.split("-")[0]), int(end.split("-")[1])
            lookup[item["subsidy_type"]] = (sm, sd, em, ed)
    return lookup

def get_all_directions() -> list[str]:
    """Список всех направлений из Правил."""
    return list(NORMATIVES.keys())

if __name__ == "__main__":
    norm_lookup = build_normative_lookup()
    print(f"Всего видов субсидий в справочнике: {len(norm_lookup)}")
    for stype, norm in list(norm_lookup.items())[:5]:
        print(f"  {stype}: {norm:,} тенге")